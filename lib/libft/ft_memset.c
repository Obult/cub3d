#include <stddef.h>

void	*ft_memset(void *str, int c, size_t n)
{
	size_t			i;
	unsigned char	*unsigned_str;

	i = 0;
	unsigned_str = str;
	while (i < n)
	{
		unsigned_str[i] = (unsigned char)c;
		i++;
	}
	return (str);
}
